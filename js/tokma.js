$(document).ready(function(){
	$(".header-links").hover(function(){
	  $("#option-hover").show();
	});
	
	$(".header-links").hover(function(){
	  $("#option-hover").show();
	});
	
	$(".header-links").mouseleave(function(){
	  $("#option-hover").hide();
	});
	
   $("#option-hover").hover(function(){
	   this.show();
   });
});